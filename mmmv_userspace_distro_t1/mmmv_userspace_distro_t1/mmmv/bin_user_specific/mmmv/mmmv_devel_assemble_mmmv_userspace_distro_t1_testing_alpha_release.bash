        echo ""
        echo "Something went wrong. Error code: $S_ERR_CODE"
        echo "Aborting script."
        echo "GUID=='5f2c5103-29c1-4218-b1a6-3340011067e7'"
        echo "S_GUID_CANDIDATE=='$S_GUID_CANDIDATE'"
